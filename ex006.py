# Dobro, Triplo e Raiz Quadrada
n = int(input('Digite um número: '))    # Pergunta um número e o coloca na variavel n
print('{:=^20}\nDobro {}\nTriplo {}\nRaiz Quadrada {:.2f}'.format(n, n*2, n*3, n**(1/2)))
# Mostra o dobro, triplo e a raiz do número
